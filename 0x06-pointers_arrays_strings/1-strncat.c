#include "main.h"
#include <string.h>
/**
 * *_strncat - concatenate 2 strings
 * @dest: first string
 * @src: the second string
 * @n: the maximum number of chars in the second string
 * Return: concatenated 2 strings
 */
char *_strncat(char *dest, char *src, int n)
{
	dest = strlen(dest, src[n]);
	return (dest);
}
